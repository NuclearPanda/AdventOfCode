def lpadzeroes(s: str):
    while len(s) < 5:
        s = "0" + s
    return s


def find_params(instruction: str, memory: list, instruction_pointer: int):
    params = []
    instruction = instruction[:-2][::-1]
    for i, char in enumerate(instruction):
        if i + 1 == len(instruction):
            break
        if char == "0":
            params.append(memory[memory[instruction_pointer + i + 1]])
        elif char == "1":
            params.append(memory[instruction_pointer + i + 1])
    return params


